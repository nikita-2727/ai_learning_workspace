def get_avg_probability(whisper_json):
    """
    Собирает все логиты из сегментов и считает среднее для чанка  
    """
    if not whisper_json:
        print("======= MISTAKE!!! ======= Ошибка не получен валидный json")
        return 0.0
    
    logprobs = []
    for seg in whisper_json.get("segments", []):
        avg_logprob = seg.get("avg_logprob", 0.0)
        # если данные получены, добавляем в массив
        # если получили ноль, значит, скорее всего, avg_logprob нет в этом сегменте
        if avg_logprob != 0.0:
            logprobs.append(avg_logprob)

    # нет ключа "segments"
    # если сегменты есть, но ни у одного нет реального avg_logprob
    # возвращаем минимально возможную точность
    if not logprobs:
        print("======= MISTAKE!!! ======= В переданном чанке не найден ни один логит")
        return -float('inf')
    
    # считаем и возвращаем среднее
    return sum(logprobs) / len(logprobs)



def select_best_chunks_by_type(chunks, proportions, max_total):
    """
    Отбирает чанки с наибольшей точностью по типам, соблюдая пропорции.
    Возвращает список отобранных чанков.
    """
    # Группируем по типам
    # мапа, которая создает ключ при его отсутствии
    by_type = defaultdict(list)
    for chunk in chunks:
        # на выходе получаем словарь, где ключи - это типы (term, question, noise, connector)
        # а значения это массив с чанками этого типа
        by_type[chunk["type"]].append(chunk)

    # Сортируем внутри каждого типа по убыванию avg_prob, (в начале с большей уверенностью)
    for chunk_type in by_type:
        by_type[chunk_type].sort(key=lambda x: x["avg_prob"], reverse=True)


    selected = []
    # Для каждого типа вычисляем, сколько нужно взять
    # проходим по типам и массивам
    for chunk_type, items in by_type.items():
        target_count = int(max_total * proportions.get(chunk_type, 0.0)) # получаем количество чанков для каждого типа
        if target_count == 0:
            print(f"======= MISTAKE!!! ======= чанка с таким типом нет в PROPORTIONS: {chunk_type}")
            continue
        # Берём c наибольшей уверенностью, но не больше имеющегося количества чанков
        take = min(target_count, len(items))
        selected.extend(items[:take]) # добавляем в массив нужное количество чанков
        print(f"{chunk_type}: взято {take} из {len(items)} (нужно {target_count})\n")


    # Если набрали меньше max_total (из-за нехватки каких-то типов), 
    # можно добирать лучшими из оставшихся, но пока просто предупреждаем
    if len(selected) < max_total:
        print(f"======= ОчЕнЬ ГрУсТнО ======= Набрано только {len(selected)} чанков из {max_total}. Не хватает данных.")
        print(f"==== Добор из оставшихся ==== Набираем {max_total - len(selected)} чанков с лучшим logprob независимо от типа")
        # Добираем лучшими из всех оставшихся (без учёта типа)
        remaining = [c for c in chunks if c not in selected] # получаем массив чанков, которых нет в выбранных
        remaining.sort(key=lambda x: x["avg_prob"], reverse=True) # сортируем по уверенности
        # получаем нужное количество и добавляем в массив selected
        needed = max_total - len(selected) 
        selected.extend(remaining[:needed])


    # рандомно перемешиваем их все
    random.shuffle(selected)
    return selected




def stratified_split(chunks, split_ratios=(0.8, 0.1, 0.1)):
    """
    Делит чанки на train/val/test, сохраняя исходные пропорции типов.
    Для каждого типа отдельно берёт 80% в train, 10% в val, 10% в test.
    """
    by_type = defaultdict(list)
    for chunk in chunks:
        by_type[chunk["type"]].append(chunk)

    train, val, test = [], [], []

    for chunk_type, items in by_type.items():
        # Перемешиваем чанки внутри типа
        random.shuffle(items)
        # получаем количество чанков определенного типа
        n = len(items)
        
        # считаем количество чанков для сохранения пропорций
        # т к наш датасет уже разбит в нужных пропорциях, то можно брать ту же долю от определенного типа
        """
        если 500 чанков

        Train (80%):
            200 терминов (250 * 0.8)
            120 связок (150 * 0.8)
            60 вопросов (75 * 0.8)
            20 шума (25 * 0.8)
            Итог: 400 чанков (80% от 500)
        Val (10%):
            25 терминов, 15 связок, 7 вопросов, 2 шума → 50 чанков
        Test (10%):
            25 терминов, 15 связок, 8 вопросов, 3 шума → 51 чанк
        """
        n_train = int(n * split_ratios[0])
        n_val = int(n * split_ratios[1])

        # распределяем каждый тип по пропорциям
        train.extend(items[:n_train])
        val.extend(items[n_train:n_train+n_val])
        test.extend(items[n_train+n_val:])

    # Перемешиваем финальные наборы
    random.shuffle(train)
    random.shuffle(val)
    random.shuffle(test)

    return {"train": train, "val": val, "test": test}



def prepare_hf_dataset(splits: dict, output_dir: str):
    """
    Преобразует датасет в формат Hugging Face.
    
    splits: словарь с ключами 'train', 'val', 'test'
            и значениями — списками чанков с полем 'audio'
    output_dir: папка для нового датасета
    """
    # создаем папку для датасета
    hf_dir = Path(output_dir)
    hf_dir.mkdir(parents=True, exist_ok=True)

    for split_name, chunks in splits.items():
        # Создаём папку для этого сплита, внутри нее папку аудио
        split_dir = hf_dir / split_name
        audio_dir = split_dir / "audio"
        audio_dir.mkdir(parents=True, exist_ok=True)

        # список новых чанков с обновленными путями к аудио
        new_chunks = []
        audio_name_count = 0 # для нового названия аудио
        for chunk in chunks:
            audio_path = Path(chunk["audio"])
            if not audio_path.exists():
                print(f"======= MISTAKE!!! ======= Файл не найден: {audio_path}")
                continue

            # Новое имя файла
            new_filename = f"{split_name}_{audio_name_count}{audio_path.suffix}"
            new_audio_path = audio_dir / new_filename

            # Копируем файл из старого датасета в новый
            shutil.copy2(audio_path, new_audio_path)

            # Обновляем путь в чанке
            chunk_copy = chunk.copy()
            chunk_copy["audio"] = str(new_audio_path.relative_to(hf_dir))

            # добавляем обновленный чанк в массив и обновляем счетчик для аудио
            new_chunks.append(chunk_copy)
            audio_name_count += 1
        

        # Сохраняем metadata.jsonl
        metadata_path = split_dir / "metadata.jsonl"
        with open(metadata_path, 'w', encoding='utf-8') as f:
            for chunk in new_chunks:
                f.write(json.dumps(chunk, ensure_ascii=False) + '\n')

        print(f"======= Сплит датасета был создан ======= {split_name}: {len(new_chunks)} чанков сохранено в {split_dir}")




