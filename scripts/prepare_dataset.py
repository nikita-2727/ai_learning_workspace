def classify_chunk(text):
    """
    Классифицирует тип контента в чанке (term, question, noise, connector)
    """
    text_lower = text.lower() # приводим к единому регистру

    # считаем количество слов, которые попались в тексте для каждого типа
    term_count = sum(1 for t in TERM_KEYWORDS if t in text_lower)
    connector_count = sum(1 for c in CONNECTOR_KEYWORDS if c in text_lower)
    question_count = sum(1 for q in QUESTION_WORDS if q in text_lower)
    noise_count = sum(1 for n in NOISE_WORDS if n in text_lower)

    # если слов этого типа больше, чем слов других типов, значит возвращаем этот тип
    # Собираем пары (тип, счётчик)
    counts = [
        ("term", term_count),
        ("connector", connector_count),
        ("question", question_count),
        ("noise", noise_count)
    ]
    # Сортируем по убыванию счётчика, при равенстве — приоритет как в списке (term > connector > question > noise)
    counts.sort(key=lambda x: x[1], reverse=True)

    best_type, best_count = counts[0]
    if best_count == 0:
        return "connector"   # если вообще нет ни одного ключевого слова
    
    return best_type



